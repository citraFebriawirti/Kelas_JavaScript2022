/*variabel ada dua yaitu let dan const 
penggunaan let untuk apabila data yang kita simpan itu akan kita ubah
pengunaan const bertujuan apabila data yang kita simpan itu tetap atau konstan dan tidak akan di ubah*/

// penulisan let
let a = 'ayam Let';
console.log(a);

let a = 'Angsa Let';
console.log(a);
/*-----Ouput : Angsa Let----*/

// penulisan const
const b = 'ayam Const';
console.log(b);

const b = 'sapi Const';
console.log(b);
/*----Output : error, karena variable yang disimpan berjenis const yang tetap */